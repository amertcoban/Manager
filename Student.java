package ödevAboutInheritance;

public class Student extends Default {
	
	private String studentNumber;
	private String studentColour;
	private String studentJob ;
	
	public String getStudentNumber() {
		return studentNumber;
	}
	public void setStudentNumber(String studentNumber) {
		this.studentNumber = studentNumber;
	}
	public String getStudentColour() {
		return studentColour;
	}
	public void setStudentColour(String studentColour) {
		this.studentColour = studentColour;
	}
	public String getStudentJob() {
		return studentJob;
	}
	public void setStudentJob(String studentJob) {
		this.studentJob = studentJob;
	}
}
